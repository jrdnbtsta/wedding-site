// Content Display
function setTitle(region) {
  switch (region) {
    case 'north':
      return 'North Shore';
    case 'east':
      return 'East Side';
    case 'downtown':
      return 'Downtown / Lihue';
    default:
      return '';
  }
}

function createImage(region) {
  switch (region) {
    case 'north':
      return <img src={hanalei} alt="" />;
    case 'east':
      return <img src={wailua} alt="" />;
    default:
      return <img src={pic01} alt="" />;
  }
}

